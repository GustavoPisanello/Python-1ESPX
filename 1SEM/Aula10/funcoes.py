def soma(x, y):
    """Sums the values"""
    return x + y

def sub(x, y):
    """Subtract the values"""
    return x - y

def mult(x, y):
    """Multiplies the values"""
    return x * y

def div(x, y):
    """Divides the values"""
    return x / y

print(soma(1, 2))
print(sub(1, 2))
print(mult(1, 2))
print(div(1, 2))